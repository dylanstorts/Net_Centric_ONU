            <p id="lichtenberg_title">Lichtenberg Figures in a Nut-Shell</p>
            <p id="lichtenberg_body">
                Lichtenberg figures (German Lichtenberg-Figuren), or "Lichtenberg
                dust figures", are branching electric discharges that sometimes 
                appear on the surface or in the interior of insulating materials. 
                Lichtenberg figures are often associated with the progressive 
                deterioration of high voltage components and equipment. The study 
                of planar Lichtenberg figures along insulating surfaces and 3D 
                electrical trees within insulating materials often provides engineers 
                with valuable insights for improving the long-term reliability of 
                high voltage equipment. Lichtenberg figures are now known to occur 
                on or within solids, liquids, and gases during electrical breakdown.
                <a href="https://en.wikipedia.org/wiki/Lichtenberg_figure">See more on Wiki</a>
            </p>
